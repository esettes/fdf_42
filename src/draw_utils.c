#include "fdf.h"

void	set_sx(int x0, int x1, int *sx)
{
	if (x0 < x1)
		*sx = 1;
	else
		*sx = -1;
}

void	set_sy(int y0, int y1, int *sy)
{
	if (y0 < y1)
		sy = 1;
	else
		sy = -1;
}